# Your program must follow spiral pattern.  

# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine

# ------------------------------------------------------------------
# Name      : vbsme
# Purpose   : This function finds the section of the frame that 
#             resembles the provided window the closest
#
# Arguments : a0 = asize (frame_y, frame_x, window_y, window_x)
#             a1 = frame
#             a2 = window
#
# Return    : v0 = x-coordinate of the section with the smallest SAD
#             v1 = y-coordinate of the section with the smallest SAD
#
# Notes     : s0 = starting point index(y, x)
#             s1 = smallest coordinate index(y, x)
#             s2 = buffers (top, right, bottom, left)
#             s3 = smallest_SAD
#             s4 = direction
#             s5 = direction_changes
# ------------------------------------------------------------------

vbsme:
    addi    $v0, $v0, 0         # reset $v0 and $V1
    addi    $v1, $v1, 0

    addi    $sp, $sp, -4        # adjust stack for 1 item
    sw      $ra, 0($sp)         # save the return address

    add     $s6, $a0, $0        # s6 -> asize
    add     $s7, $a1, $0        # s7 -> frame
    add     $t9, $a2, $0        # t9 -> window

    #-------------------------------------
    # s0 -> starting_point[2] = {0,0}
    #-------------------------------------

    li      $a0, 8              # allocate space for 2 words
    li      $v0, 9
    syscall

    add     $s0, $v0, $0
    sw      $0, 0($s0)
    sw      $0, 4($s0)

    #-------------------------------------
    # s1 -> smallest_coordinate[2] = {0,0}
    #-------------------------------------

    li      $a0, 8              # allocate space for 2 words
    li      $v0, 9
    syscall
    
    add     $s1, $v0, $0
    sw      $0, 0($s1)
    sw      $0, 4($s1)
    
    #-------------------------------------
    # s2 -> buffers[4] = {0,0,0,0}
    #-------------------------------------

    li      $a0, 16              # allocate space for 4 words
    li      $v0, 9
    syscall
    
    add     $s2, $v0, $0
    sw      $0,  0($s2)
    sw      $0,  4($s2)
    sw      $0,  8($s2)
    sw      $0, 12($s2)

    #-------------------------------------
    # other variables
    #-------------------------------------

    add     $s4, $0, $0         # s4 -> direction
    add     $s5, $0, $0         # s5 -> direction_changes

    #-------------------------------------
    # initial getSAD function call
    #-------------------------------------

    add     $a0, $t9, $0        # window
    add     $a1, $s7, $0        # frame
    add     $a2, $s6, $0        # asize
    add     $a3, $s0, $0        # starting_point
    jal     getSAD

    add     $s3, $v0, $0        # cur_sad value

    addi    $v0, $v0, 0         # reset $v0

vbsmeLoop:
    
    addi    $t0, $0, 0
    beq     $t0, $s4, vbsmeDirection1

    addi    $t0, $0, 1
    beq     $t0, $s4, vbsmeDirection2

    addi    $t0, $0, 2
    beq     $t0, $s4, vbsmeDirection3

    addi    $t0, $0, 3
    beq     $t0, $s4, vbsmeDirection4



# Direction 1
#-------------

vbsmeDirection1:                # top-left to top-right

    lw      $t0,  4($s0)        # starting_point[1]
    lw      $t1, 12($s6)        # asize[3]
    lw      $t2,  4($s6)        # asize[1]
    lw      $t3,  4($s2)        # right_buffer

    add     $t1, $t0, $t1
    sub     $t2, $t2, $t3

    slt     $t1, $t1, $t2       # starting_point[0] + asize[2] < asize[0] - right_buffer
    beq     $t1, $0, vbsmeChangeDir1

    addi    $t0, $t0, 1         # starting_point[1] ++
    sw      $t0, 4($s0) 
    add     $s5, $0, $0         # direction_changes = 0;
    j       vbsmeTransition

vbsmeChangeDir1:
    
    lw      $t0, 0($s2)        
    addi    $t0, $t0, 1
    sw      $t0, 0($s2)         # top_buffer ++ 

    addi    $s4, $0, 1          # direction = 1;
    addi    $s5, $s5, 1         # direction_changes ++;
    j       vbsmeTransition


#-------------
# Direction 2
#-------------

vbsmeDirection2:                # top-right to bottom-right

    lw      $t0,  0($s0)        # starting_point[0]
    lw      $t1,  8($s6)        # asize[2]
    lw      $t2,  0($s6)        # asize[0]
    lw      $t3,  8($s2)        # bottom_buffer

    add     $t1, $t0, $t1
    sub     $t2, $t2, $t3

    slt     $t1, $t1, $t2       # starting_point[0] + asize[2] < asize[0] - bottom_buffer
    beq     $t1, $0, vbsmeChangeDir2

    addi    $t0, $t0, 1         # starting_point[0] ++
    sw      $t0, 0($s0) 
    add     $s5, $0, $0         # direction_changes = 0;
    j       vbsmeTransition

vbsmeChangeDir2:
    
    lw      $t0, 4($s2)        
    addi    $t0, $t0, 1
    sw      $t0, 4($s2)         # right_buffer ++ 

    addi    $s4, $0, 2          # direction = 2;
    addi    $s5, $s5, 1         # direction_changes ++;
    j       vbsmeTransition


#-------------
# Direction 3
#-------------

vbsmeDirection3:                # bottom-right to bottom-left

    lw      $t0,  4($s0)        # starting_point[1]
    lw      $t1,  12($s2)       # left_buffer

    slt     $t1, $t1, $t0       # left_buffer < starting_point[1]
    beq     $t1, $0, vbsmeChangeDir3

    addi    $t0, $t0, -1        # starting_point[1] --
    sw      $t0, 4($s0) 
    add     $s5, $0, $0         # direction_changes = 0;
    j       vbsmeTransition

vbsmeChangeDir3:
    
    lw      $t0, 8($s2)        
    addi    $t0, $t0, 1
    sw      $t0, 8($s2)         # bottom_buffer ++ 

    addi    $s4, $0, 3          # direction = 3;
    addi    $s5, $s5, 1         # direction_changes ++;
    j       vbsmeTransition


#-------------
# Direction 4
#-------------

vbsmeDirection4:                # bottom-left to top-left

    lw      $t0,  0($s0)        # starting_point[0]
    lw      $t1,  0($s2)        # top_buffer

    slt     $t1, $t1, $t0       # top_buffer < starting_point[0]
    beq     $t1, $0, vbsmeChangeDir4

    addi    $t0, $t0, -1        # starting_point[0] --
    sw      $t0, 0($s0) 
    add     $s5, $0, $0         # direction_changes = 0;
    j       vbsmeTransition

vbsmeChangeDir4:
    
    lw      $t0, 12($s2)        
    addi    $t0, $t0, 1
    sw      $t0, 12($s2)        # left_buffer ++ 

    addi    $s4, $0, 0          # direction = 0;
    addi    $s5, $s5, 1         # direction_changes ++;


#------------
# Transition 
#------------

vbsmeTransition:

    beq     $s5, $0, vbsmeGetSAD

    addi    $t0, $0, 4
    beq     $s5, $t0, vbsmeEnd

    j       vbsmeLoop


#-------------
# Get New SAD 
#-------------

vbsmeGetSAD:

    #-------------------------------------
    # getSAD function call
    #-------------------------------------

    add     $a0, $t9, $0        # window
    add     $a1, $s7, $0        # frame
    add     $a2, $s6, $0        # asize
    add     $a3, $s0, $0        # starting_point
    jal     getSAD

    add     $t0, $v0, $0        # cur_sad value
    addi    $v0, $v0, 0         # reset $v0

    #-------------
    # Compare SAD
    #-------------

    slt     $t1, $t0, $s3
    beq     $t1, $0, vbsmeLoop  # if cur_SAD >= smallest_SAD -> loop again

    add     $s3, $t0, $0        # smallest_SAD = cur_SAD;

    lw      $t1, 0($s0)
    sw      $t1, 0($s1)         # smallest_coord[0] = starting_point[0]

    lw      $t1, 4($s0)
    sw      $t1, 4($s1)         # smallest_coord[1] = starting_point[1]

    bne     $t0, $0, vbsmeLoop  # if current SAD != 0 -> loop again

#-----------
# End vbsme 
#-----------

vbsmeEnd:

    lw      $ra, 0($sp)         # restore the return address
    addi    $sp, $sp, 4         # adjust stack pointer to pop 1 item

    lw      $v1, 4($s1)
    lw      $v0, 0($s1)

    jr      $ra


# ------------------------------------------------------------------
# Name      : getSAD
# Purpose   : This function returns the SAD value between the 
#             comparison array and the provided window array
#
# Arguments : a0 = window
#             a1 = frame
#             a2 = asize
#             a3 = starting_point
#
# Return    : v0 = SAD value
# ------------------------------------------------------------------

getSAD:

    lw      $t2, 8($a2)             # asize[2]
    lw      $t3, 12($a2)            # asize[3]

    add     $t0, $0, $0             # t0 = sum
    add     $t1, $0, $0             # t1 = iterator

    lw      $t4, 0($a3)             # t4 = y = starting_point[0]
    lw      $t5, 4($a3)             # t5 = x = starting_point[1]

    mul     $t2, $t2, $t3           # t2 = asize[2] * asize[3]

    addi    $t3, $t3, -1            # t3 = asize[3] - 1

    add     $t6, $0, $0             # t6 = comp_index

getSADLoop:

    lw      $t7, 4($a2)            
    mul     $t7, $t7, $t4 
    add     $t6, $t7, $t5           # comp_index = x + (y * asize[1])

    sll     $t7, $t1, 2
    add     $t7, $t7, $a0
    lw      $t7, 0($t7)             # window[i]

    sll     $t8, $t6, 2
    add     $t8, $t8, $a1
    lw      $t8, 0($t8)             # frame[comp_index]
 
    sub     $t7, $t7, $t8           
    abs     $t7, $t7                
    add     $t0, $t0, $t7           # sum += abs(window[i] - frame[comp_index])

    lw      $t7, 4($a3) 
    sub     $t7, $t5, $t7
    slt     $t7, $t7, $t3           # x - starting_point[1] < asize[3] - 1
    beq     $t7, $0, getSADNewRow

    addi    $t5, $t5, 1             # x++

    add     $t1, $t1, 1             # i++

    slt     $t7, $t1, $t2
    beq     $t7, $0, getSADEnd     # if iterator < length -> getSADEnd
    j       getSADLoop

getSADNewRow:
    
    lw      $t5, 4($a3)             # x = starting_point[1]
    addi    $t4, $t4, 1             # y++
    
    add     $t1, $t1, 1             # i++
    beq     $t1, $t2, getSADEnd     # if iterator == asize[2] * asize[3] -> getSADEnd
    j       getSADLoop 
 
getSADEnd: 
 
    add     $v0, $t0, $0            # return sum (t0)
    jr      $ra
   
